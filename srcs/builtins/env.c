#include "minishell.h"

void	env(char **envp)
{
	int		i;

	i = 0;
	while (envp[i])
	{
		write(1, envp[i], ft_strlen(envp[i]));
		write(1, "\n", 1);
		i++;
	}
	sg()->ret_exit = 0;
}
